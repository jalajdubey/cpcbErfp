<h5 class="mb-3">Stakeholders Involved in the ERF Scheme</h5>
<ul class="list-unstyled mb-5">
  <li class="mb-3 ps-2">
    <strong>MoEF&amp;CC:</strong> The central regulatory authority that notifies and oversees the Environment Relief Fund framework and policies.
  </li>
  <li class="mb-3 ps-2">
    <strong>Industries:</strong> Handling hazardous substances and required to obtain public liability insurance and contribute to the ERF.
  </li>
  <li class="mb-3 ps-2">
    <strong>CPCB:</strong> Designated fund manager responsible for managing the ERF and disbursing compensation to victims as per sanctioned claims.
  </li>
  <li class="mb-3 ps-2">
    <strong>District Collectors / Local Authorities:</strong> Act as the nodal point for processing and forwarding claims submitted by victims.
  </li>
  <li class="mb-3 ps-2">
    <strong>Victims:</strong> Individuals or communities affected by accidents involving hazardous substances who are eligible to receive compensation from the ERF.
  </li>
  <li class="mb-3 ps-2">
    <strong>Pollution Control Boards (SPCBs / PCCs):</strong> Monitor compliance of industries with environmental safety standards and support enforcement of legal provisions related to hazardous substances.
  </li>
</ul>

