     <p>
    The Public Liability Insurance (PIL) Act, 1991 was enacted to provide for public
insurance ensuring immediate relief to the individuals affected by accidents involving
hazardous substances. It covers incidents arising during the handling of such hazardous
substance and addresses connected or incidental matters.
</p>
<p>
The hazardous substances refer to any substance or preparation which is defined as
hazardous substance under the Environment (Protection) Act, 1986, exceeding the
quantity limit specified by the MoEF&amp;CC in its notification S.O. 227(E) dated 24 th day of
March, 1992. Every owner handling the hazardous substance is required to obtain a
liability insurance policy in compliance of the Act.
The Environment Relief (Amended) Scheme, 2024 aims to provide financial assistance
for environmental restoration and relief in cases of industrial accidents involving

hazardous substances. It is linked to the Environment Relief Fund (ERF), which was
established under the Public Liability Insurance Act, 1991.</p>
<p>
In instances where environmental damage occurs due to manufacture, processing,
treatment, package, storage, use, collection, destruction, conversion, transfer or such
other processes, of such hazardous substance, the Central Government, upon receiving
an application from the Central Pollution Control Board or the State Pollution Control
Board, may allocate funds from the Environment Relief Fund for restoration of damage.
